# Ввод списка из вещественных элементов
n = int(input("Введите количество элементов списка: "))
A = [float(input(f"Элемент {i + 1}: ")) for i in range(n)]

# 1. Количество элементов, равных 0
count_zero = A.count(0)

# 2. Сумма элементов, расположенных после минимального элемента
min_index = A.index(min(A))
sum_after_min = sum(A[min_index + 1:])

# 3. Упорядочивание элементов по возрастанию модулей
A.sort(key=lambda x: abs(x))

# Вывод результатов
print("\nРезультаты:")
print(f"Количество элементов, равных 0: {count_zero}")
print(f"Сумма элементов после минимального элемента: {sum_after_min}")
print(f"Список после упорядочивания по возрастанию модулей: {A}")
